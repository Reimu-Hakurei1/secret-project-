let currentLang = 'en';
let forgotPasswordModal = null;
let passwordResetAttempts = 0;

document.addEventListener('DOMContentLoaded', function() {
  // Load language immediately when DOM is ready
  loadLanguage();
  initializeLoginApp();
});

function loadLanguage() {
  // Check if language is stored in localStorage
  const savedLang = localStorage.getItem('preferredLang');
  if (savedLang && loginTranslations[savedLang]) {
    currentLang = savedLang;
    console.log('ЁЯМР Loaded saved language:', currentLang);
  } else {
    // Set default to English if no saved language
    currentLang = 'en';
    localStorage.setItem('preferredLang', 'en');
    console.log('ЁЯМР Defaulting to English');
  }
}

async function initializeLoginApp() {
  // Initialize language dropdown (already have currentLang from loadLanguage)
  initializeLanguageDropdown();
  
  // Initialize Bootstrap modal
  initializeModal();
  
  // Set up event listeners
  setupEventListeners();
  
  // Check if user is already logged in
  checkExistingAuth();
  
  // Apply language immediately
  updateAllText();
}

function initializeModal() {
  const modalElement = document.getElementById('forgotPasswordModal');
  if (modalElement) {
    forgotPasswordModal = new bootstrap.Modal(modalElement);
    
    // Reset modal when hidden
    modalElement.addEventListener('hidden.bs.modal', function () {
      resetForgotPasswordForm();
    });
    
    // Reset modal when shown
    modalElement.addEventListener('show.bs.modal', function () {
      passwordResetAttempts = 0;
      resetForgotPasswordForm();
    });
  }
}

function initializeLanguageDropdown() {
  const languageMenu = document.getElementById('languageMenu');
  const currentLanguageElement = document.getElementById('currentLanguage');
  
  if (!languageMenu) return;
  
  // Clear existing options and add all languages
  languageMenu.innerHTML = '';
  
  // Add all language options in the specified order
  const languageOrder = ['en', 'zh', 'ja', 'ko', 'ar', 'hi', 'th', 'vi', 'id', 'ms', 'ru', 'ur', 'tr', 'fa', 'ta', 'te', 'bn', 'lo', 'km', 'my', 'ne', 'pa', 'si'];
  
  languageOrder.forEach(code => {
    const name = languageNames[code];
    if (!name) return;
    
    // Create button element for better styling
    const button = document.createElement('button');
    button.type = 'button';
    button.className = 'dropdown-item language-dropdown-item';
    button.dataset.lang = code;
    button.textContent = name;
    
    // Add active class if this is the current language
    if (code === currentLang) {
      button.classList.add('active');
      if (currentLanguageElement) {
        currentLanguageElement.textContent = name.split(' ')[0]; // Get language name without flag
      }
    }
    
    // Add click event
    button.addEventListener('click', function(e) {
      e.preventDefault();
      const lang = this.dataset.lang;
      console.log('ЁЯФД Language dropdown clicked:', lang);
      switchLanguage(lang);
      
      // Close the dropdown after selection
      const dropdownButton = document.getElementById('languageDropdown');
      if (dropdownButton) {
        const dropdown = bootstrap.Dropdown.getInstance(dropdownButton);
        if (dropdown) {
          dropdown.hide();
        }
      }
    });
    
    languageMenu.appendChild(button);
  });
}

function switchLanguage(lang) {
  console.log('ЁЯФД Switching language to:', lang);
  
  if (lang === currentLang) return;
  
  currentLang = lang;
  localStorage.setItem('preferredLang', lang);
  
  // Update dropdown selection
  updateLanguageDropdown();
  
  // Update all text content
  updateAllText();
  
  console.log('тЬЕ Language switched to:', lang);
}

function updateLanguageDropdown() {
  const languageItems = document.querySelectorAll('#languageMenu .dropdown-item');
  const currentLanguageElement = document.getElementById('currentLanguage');
  
  languageItems.forEach(item => {
    item.classList.remove('active');
    if (item.dataset.lang === currentLang) {
      item.classList.add('active');
      if (currentLanguageElement) {
        currentLanguageElement.textContent = item.textContent.split(' ')[0];
      }
    }
  });
}

function updateAllText() {
  const langData = loginTranslations[currentLang];
  
  if (!langData) {
    console.error('тЭМ No translation data for language:', currentLang);
    return;
  }
  
  console.log('ЁЯУЭ Updating text for language:', currentLang);
  
  // Update page title
  updatePageTitle();
  
  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (langData[key]) {
      element.textContent = langData[key];
    }
  });
  
  // Update all elements with data-i18n-placeholder attribute
  document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
    const key = element.getAttribute('data-i18n-placeholder');
    if (langData[key]) {
      element.placeholder = langData[key];
    }
  });
  
  // Update error messages
  updateErrorMessages();
  
  console.log('тЬЕ All text updated for language:', currentLang);
}

function updatePageTitle() {
  const titleElement = document.querySelector('title');
  const key = 'login_title';
  if (loginTranslations[currentLang] && loginTranslations[currentLang][key]) {
    titleElement.textContent = loginTranslations[currentLang][key] + ' - STIU Student Portal';
  }
}

